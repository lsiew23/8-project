    <br>
    <center style="font-size:2vw">
    Cyber Hawks is a technology club at Hanover High School, established in 2012. 
    Our mission is to leverage our IT and programming skills to make a positive impact in our community, one bit at a time.
    We meet every Tuesday to discuss technology-related challenges and brainstorm innovative solutions.
    Additionally, we are committed to giving back to our community. 
    Once a month, we visit the council of aging to provide tech support to seniors. 
    It's a rewarding experience that allows us to share our knowledge and give back to the community that supports us.
    Finally, we believe that learning and fun should go hand in hand. 
    That's why we host game days on the first Tuesday of every month from 5:00pm to 6:00pm. 
    It's a chance to unwind and have some fun while building camaraderie among our members. 
    Join us and be part of the Cyber Hawks community!
    
